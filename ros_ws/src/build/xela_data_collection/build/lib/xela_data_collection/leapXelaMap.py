def bracket_representation(
    grid: np.ndarray,
    *,
    red: str = "\x1b[31m",
    green: str = "\x1b[32m",
    reset: str = "\x1b[0m",
    cell: str = "[]",
    joiner: str = "",
    use_color: bool = True,
) -> str:
    arr = np.asarray(grid)
    if arr.ndim != 2:
        raise ValueError(f"grid must be 2D, got shape {arr.shape}")

    if not use_color:
        return "\n".join(joiner.join(cell for _ in range(arr.shape[1])) for _ in range(arr.shape[0]))

    rcell = f"{red}{cell}{reset}"
    gcell = f"{green}{cell}{reset}"

    lines: list[str] = []
    for row in arr:
        parts = [gcell if int(v) == 1 else rcell for v in row]
        lines.append(joiner.join(parts))
    return "\n".join(lines)



